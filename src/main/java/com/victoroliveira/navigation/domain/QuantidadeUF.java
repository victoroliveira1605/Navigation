package com.victoroliveira.navigation.domain;

public class QuantidadeUF {
    private String UF;
    private int qtd;

    public QuantidadeUF() {
    }

    public String getUF() {
        return UF;
    }

    public void setUF(String UF) {
        this.UF = UF;
    }

    public int getQtd() {
        return qtd;
    }

    public void setQtd(int qtd) {
        this.qtd = qtd;
    }
}
