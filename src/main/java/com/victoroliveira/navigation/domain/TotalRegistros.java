package com.victoroliveira.navigation.domain;

public class TotalRegistros {
    private String msg;
    private int qtd;

    public TotalRegistros() {
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getQtd() {
        return qtd;
    }

    public void setQtd(int qtd) {
        this.qtd = qtd;
    }
}
